        void PushChannel_ShellToastNotificationReceived(object sender, NotificationEventArgs e)
        {
            StringBuilder message = new StringBuilder();
            string relativeUri = string.Empty;

            message.AppendFormat("App: Received Toast {0}:\n", DateTime.Now.ToShortTimeString());

            // Parse out the information that was part of the message.
            foreach (string key in e.Collection.Keys)
            {
                message.AppendFormat("{0}: {1}\n", key, e.Collection[key]);

                if (string.Compare(
                    key,
                    "wp:Param",
                    System.Globalization.CultureInfo.InvariantCulture,
                    System.Globalization.CompareOptions.IgnoreCase) == 0)
                {
                    relativeUri = e.Collection[key];
                }
            }

            // Display a dialog of all the fields in the toast.
            Debug.WriteLine(message.ToString());

            Deployment.Current.Dispatcher.BeginInvoke(() => 
            {
                var currentPage = App.RootFrame.Content as MainScreen;
                if (currentPage != null)
                {
                    currentPage.ShowPushToastMessage(e.Collection["wp:Text1"], e.Collection["wp:Text2"]);
                }
                //Debug.WriteLine(currentPage.Title);
            });
            

            
        }


